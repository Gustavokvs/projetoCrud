     public void gravarGenero() {
        Genero genero = new Genero();

        genero.setId(id);
        genero.setNome(edtNome.getText().trim());

        if (controller.atualizar(genero)) {
            JOptionPane.showMessageDialog(null, "Gênero atualizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Erro: Gênero não pôde ser atualizado.");
        }
    }

    public void carregarGenero() {
        Genero genero = controller.buscarPorId(id);

        edtCodigo.setText(String.valueOf(genero.getId()));
        edtNome.setText(genero.getNome());

    }
