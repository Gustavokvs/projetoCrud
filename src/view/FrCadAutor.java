    public void salvar() {
        String nome = edtNomeAutor.getText().trim();

        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, insira o nome do autor.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Autor autor = new Autor();
        autor.setNome(nome);

        AutorController controller = new AutorController();
        try {
            if (controller.inserir(autor)) {
                JOptionPane.showMessageDialog(this, "Autor salvo com sucesso!");
                dispose(); // fecha a janela ap√≥s salvar
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar autor.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar autor: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

