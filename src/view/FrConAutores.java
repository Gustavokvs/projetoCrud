    public void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblAutores.getModel();
        modelo.setRowCount(0); // limpa a tabela

        AutorController controller = new AutorController();

        try {
            for (Autor autor : controller.listarTodos()) {
                modelo.addRow(new Object[]{autor.getId(), autor.getNome()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar autores: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    private void excluirAutor() {
        int linhaSelecionada = tblAutores.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um autor para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este autor?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (confirmacao == JOptionPane.YES_OPTION) {
            int id = (int) tblAutores.getValueAt(linhaSelecionada, 0);
            AutorController controller = new AutorController();
            if (controller.excluir(id)) {
                JOptionPane.showMessageDialog(this, "Autor excluído com sucesso!");
                carregarTabela();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir autor.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void editarAutor() {
        int linhaSelecionada = tblAutores.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um autor para editar.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int id = (int) tblAutores.getValueAt(linhaSelecionada, 0);
        String nomeAtual = (String) tblAutores.getValueAt(linhaSelecionada, 1);

        String novoNome = JOptionPane.showInputDialog(this, "Editar nome do autor:", nomeAtual);

        if (novoNome != null && !novoNome.trim().isEmpty()) {
            Autor autor = new Autor(id, novoNome);
            AutorController controller = new AutorController();
            if (controller.atualizar(autor)) {
                JOptionPane.showMessageDialog(this, "Autor atualizado com sucesso!");
                carregarTabela();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar autor.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else if (novoNome != null) {
            JOptionPane.showMessageDialog(this, "O nome do autor não pode estar vazio.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }

