    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        if (tblConFuncionario.getSelectedRow() != -1) {
            //se tiver selecionada uma linha pega o código da grade/linha
            int linhaSelecionada = tblConFuncionario.getSelectedRow();
            String textoCelula = tblConFuncionario.getValueAt(linhaSelecionada, 0).toString();

            //converter o texto da célula em inteiro
            int id = Integer.parseInt(textoCelula);

            //com o PkUsuario eu vou chamar um método de deletar no controller
           FuncionarioController controller = new FuncionarioController();
            if (controller.excluir(id)) {
                pesquisar();//pesquiso primeiro e depois aparece se foi deletado ou não
                JOptionPane.showMessageDialog(rootPane, "Deletado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não foi deletado");

            }

        }
    }//GEN-LAST:event_btnExcluirMouseClicked

    public void pesquisar() {
        DefaultTableModel modeloTabela = (DefaultTableModel) tblConFuncionario.getModel();

        modeloTabela.setNumRows(0);

        FuncionarioController controller = new FuncionarioController();

        ArrayList<Funcionario> listarTodos = controller.listarTodos();
        
        for (Funcionario func : listarTodos) {
            Object[] linha = {
                func.getId(), 
                func.getNome(), 
                func.getCargo(),
                func.getEmail(), 
                func.getTelefone(),
                func.ativoToString() 
            };

            modeloTabela.addRow(linha);
        }
    }
    
