    private void btnAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAlterarMouseClicked
        if (tblConFuncionario.getSelectedRow() != -1) {
        int linhaSelecionada = tblConFuncionario.getSelectedRow();
        String textoCelula = tblConFuncionario.getValueAt(linhaSelecionada, 0).toString();
        int id = Integer.parseInt(textoCelula);

        FrAltFuncionario telaAlt = new FrAltFuncionario(null, rootPaneCheckingEnabled, id);
        telaAlt.setVisible(true);
        
        pesquisar(); // atualiza os dados após alteração
    }
    }//GEN-LAST:event_btnAlterarMouseClicked

